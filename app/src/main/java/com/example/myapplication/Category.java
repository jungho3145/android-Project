package com.example.myapplication;

public class Category {
    String categoriName;
    String color;


    public Category() {

    }

    public String getCategori() {
        return categoriName;
    }

    public void setCategori(String categori) {
        this.categoriName = categori;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }
}
